

export default (
    (c) => {
        return (
            (c === 46)
            || ((c >= 48) && (c <= 57))
            || ((c >= 65) && (c <= 70))
            || (c === 79)
            || (c === 88)
            || (c === 95)
            || ((c >= 97) && (c <= 102))
            || (c === 111)
            || (c === 120)
            || (c === 110)
        );
    }
);
